package modelo;

public class User {

	private String login;
	private String senha;
	
	public String getNome() {
		return login;
	}
	
	public void setNome(String login) {
		this.login = login;
	}
	
	public String getSenha() {
		return senha;
	}
	
	public void setSenha(String senha) {
		this.senha = senha;
	}
	
	
}
